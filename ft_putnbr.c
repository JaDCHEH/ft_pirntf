#include "ft_printf.h"

int	ft_putnbr(int nbr)
{
	long	n;
	int		a;

	n = (long) nbr;
	a = ft_numlen(n);
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	ft_putchar((n % 10) + '0');
	return (a);
}
