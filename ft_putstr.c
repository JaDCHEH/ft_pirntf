#include "ft_printf.h"

int	ft_putstr(const char	*s)
{
	int	i;
	int	a;

	i = 0;
	a = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			a = a + ft_putchar(s[i]);
			i++;
		}
	}
	else
	{
		a = ft_putstr("(null)");
	}
	return (a);
}
